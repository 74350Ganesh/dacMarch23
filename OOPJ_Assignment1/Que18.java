class Que18{
   /* Write a program to find minimum and maximum number 
   as well as to add two integer numbers using methods of Integer.*/

    public static void main(String [] args){
        int num1=10;
        int num2=20;
        System.out.println("find minimum number: "+Integer.min(num1,num2));
        System.out.println("find maximum number: "+Integer.max(num1,num2));
        System.out.println("Sum of two integer number: "+Integer.sum(num1,num2));
    }
}